# numerical_operations_the_return.py

def modulo(a, b):
    """Return a % b, or 0 if b is 0."""
    if b == 0:
        return 0
    return a % b

def divide(a, b):
    """Return a / b, or 0 if b is 0."""
    if b == 0:
        return 0
    return a / b

def integer_division(a, b):
    """Return a // b, or 0 if b is 0."""
    if b == 0:
        return 0
    return a // b
